#ifndef __BEEP_H
#define	__BEEP_H

#include "stm32f10x.h"


extern uint16_t beep_counter;//为扬声器提供节拍
extern FunctionalState beep_count_state;
extern uint8_t meal_voice_time;//语音播放次数

/* 定义蜂鸣器连接的GPIO端口, 用户只需要修改下面的代码即可改变控制的蜂鸣器引脚 */
#define BEEP_GPIO_PORT    	GPIOA			              /* GPIO端口 */
#define BEEP_GPIO_CLK 	    RCC_APB2Periph_GPIOA		/* GPIO端口时钟 */
#define BEEP_GPIO_PIN		  	GPIO_Pin_8			        /* 连接到蜂鸣器的GPIO */



#define digitalToggle(p,i) {p->ODR ^=i;} //输出反转状态

#define BEEP_TOGGLE		 digitalToggle(BEEP_GPIO_PORT,BEEP_GPIO_PIN)

/* 带参宏，可以像内联函数一样使用 */
#define BEEP(a)	if (a)	\
					GPIO_SetBits(BEEP_GPIO_PORT,BEEP_GPIO_PIN);\
					else		\
					GPIO_ResetBits(BEEP_GPIO_PORT,BEEP_GPIO_PIN)

void BEEP_GPIO_Config(void);
void meal_voice(uint8_t n);
					
#endif /* __BEEP_H */

					
